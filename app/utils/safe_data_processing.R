generar_df_vacio <- function(fid) {
  # Obtener la estructura de columnas del formulario
  if (fid == "ddc_coPr_CapIns") {
    cols <- cols_base_CO_capa
  } else if (fid == "ddc_coPr_StdyPrtc") {
    cols <- cols_base_CO_esta
  } else if (fid == "ddc_coTel") {
    cols <- cols_base_CO_tele
  } else if (fid == "ddc_coVrt") {
    cols <- cols_base_CO_virt
  } else if (fid == "ddc_pqrsd") {
    cols <- cols_base_pqrds
  } else if (fid == "ddc_CalProc") {
    cols <- cols_base_proceso
  } else if (fid == "ddc_SatExp_p4") {
    cols <- cols_base_SatExp_pres
  } else if (fid == "ddc_SatExp_ct") {
    cols <- cols_base_SatExp_tele
  } else if (fid == "ddc_SatExp_cv") {
    cols <- cols_base_SatExp_virt
  } else if (fid == "habilidades") {
    cols <- cols_habilidades
  } else if (fid == "universe") {
    cols <- cols_universe
  } else {
    stop("Formulario no reconocido para generar dataframe vacío.")
  }
  
  as.data.frame(
    setNames(
      replicate(length(cols), NA, simplify = FALSE),
      cols
    )
  )[1, ]   # <- 1 filas
}

